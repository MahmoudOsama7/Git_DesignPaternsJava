package com.company;

public class Projector {
    public void on()
    {
        System.out.println("Projector is On..");
    }
    public void off()
    {
        System.out.println("Projector is OFF..");
    }
    public void tvMode()
    {
        System.out.println("Projector Now Is On TV Mode..");
    }
    public void wideScreenMode()
    {
        System.out.println("Projector Now Is On Wide Screen Mode..");
    }
}
